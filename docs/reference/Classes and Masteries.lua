--Attributes
-- Primary Attributes> One point is gained every level up to level 4, and then every two levels.
Strenght: Basic brute force. Aside from allowing the use of certain equipment, it also allows the use of a pick to forcely clear some sealed off paths of the dungeon.
Dexterity: It's the level of manual aptitude, allowing the use of lock picks to open locked doors of the dungeon and also to interact with traps.
Intelligence: It consists of the character's ability to understand and learn magic and also how to wield certain equipment. While in the dungeon, it allows the interaction with certain ancient artifacts.

-- Secondary Attributes> One point is gained every two levels.
Luck: The character's luck increases its basic critical rate and also improves the loot found in the dungeon.
Constitution: Constitution increases the healing from Recover while also reducing it's SP cost, and also reduces the SP cost and increase the duration of Sprint.
Wisdom: Wisdom increases the mana gain from Recharge while also reducing it's SP cost, and also fastens cast speed.
Spirit: This measures the character's will, resulting in SP recovery on every basic attack and improved effects on the use of potions and herbs.

--A level is gained every time a floor is cleared for the first time by the character.

--Classes: Specializations will be granted by a special NPC at the end of the 5th floor.

Adventurer: It is the starting class for every player, which is capable of training every skill and magic level. 
