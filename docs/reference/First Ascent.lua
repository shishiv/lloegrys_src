--Method
-Entrance portal only works every 15 minutes.
-Challenger gets a storage that will be checked at the end of the duration to kick him from the tower. Logging out disables the storage.
-Enemies have their ascent counterparts that disappear 2 minutes after being summoned.
-Floor switches summons enemy groups and activate the portal to the next floor but it can only be accessed while being out of combat, requiring the challenger to clear the floor.
-Reward Chests countain one random weapon, one random piece of armor, occasionally an accessory and/or a potion (influenced by luck). They can be accessed while out of combat.
-Every floor has a fountain that recovers HP, MP and SP. They can be accessed while out of combat.


--First Ascent, solo tower


-You can only enter without wearing any equipment.
-Only one person at a time with a time limit.
-You are given a starter set of your choice while inside by a void gatekeeper npc.
-You have to consecutively beat a series of levels. Each level will present groups of enemies of increasing difficulty and reward random pieces of equipment.
-Enemy pool includes the usual tower enemies, but occasional enemies from The Deep.
-Last floor countains a boss from The Deep.
-There are 3 different difficulty levels, where you can only access the next by beating the prior.
-Last floor rewards include fame and resources obtainable uniquely from this method. They can be used to increase HP, MP, Cap, Physical or Magical damage.




