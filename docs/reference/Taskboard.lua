Taskboard System

--Definition
Lloegrys has an achievement system where it will reward players with Fame points according to certain milestones completed within the tower. These milestones include: Completing
floors for the first time, accessing special secret rooms, interacting with artifact chests, killing certain ammounts of specific enemies or fetching rare loot from them, and, lastly,
completing the Ascent towers.

--Fame
Fame is a resource that can be farmed indefinitely and can be spent on:
-First class: 30 points
-Second class: 100 points
-Extra secondary points: 100 + 50 for every time
-Access to the Luxury Shop where you can buy advanced gear including exclusives you cant find on the tower: 100
-Access to The First Ascent: 5
-Access to The Second Ascent: 20
-Buying a Rebirth Potion: 100
-Buying a Phoenix Charm: 20
-Buying 200 coins: 20

